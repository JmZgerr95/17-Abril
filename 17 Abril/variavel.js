// Variável do tipo String (ou seja, está dentro de aspas)
let nome = "Joao Michael";
//Variável tipo Number / Int (número ou inteiro)
let idade = 18;
//Variável do tipo Boleano (true ou false)
let casado = false;

console.log("o meu nome é " + nome + "e a minha idade é " + idade + " anos."  ) // concatenando = separar (o + faz isso)

console.log(`O meu nome é ${nome} e a minha idade é ${idade} anos.`) // O sifrão serve para chamar a variável (e isso só funciona com a crase no lugar das aspas.)

console.log("Casado? " + casado);